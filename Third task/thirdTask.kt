class JDoodle { // код написан для возможности компиляции на платформе jdoodle
    // companion object - аналог static в Java
    companion object {
        // @JvmStatic - аннотация для совместимости с Java
        // Говорит компилятору создать настоящий static метод
        @JvmStatic
        fun main(args: Array<String>) {  // Точка входа в программу
            val numbers = arrayOf("124", "9829", "9872", "2", "456")
            
            // Счётчик чисел, у которых сумма цифр больше 10
            var counter = 0
            
            // Внешний цикл: перебираем все числа в массиве
            for (num in numbers) {
                // Переменная для хранения суммы цифр текущего числа
                var sum = 0
                
                // Внутренний цикл: перебираем каждый символ в числе
                for (ch in num) {
                    sum += ch.toString().toInt()
                }
                
                // Проверяем условие: сумма цифр больше 10
                if (sum > 10) {
                    counter++ }}
            // Выводим исходный массив
            // joinToString(", ") - склеивает элементы массива через запятую с пробелом
            println("Массив: ${numbers.joinToString(" ")}")
            
            // Выводим результат
            // $counter - подставляет значение переменной в строку
            println("Результат: $counter")
        }
    }
}
